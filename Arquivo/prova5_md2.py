def division_by_subtraction_verified(a: int, b: int) -> tuple:
    """
    Calcula q = a // b  e  r = a % b  por subtração sucessiva.
    Totalmente instrumentada com asserções em tempo de execução.
    """

    # # 1. ASSERCAO DE PRE-CONDICAO
    assert a >= 0 and b > 0, "Erro: Pre-condicao violada!"

    # Estado imutável para validação do invariante
    a_orig = a

    # Inicialização das variáveis de estado
    y = 0  # Quociente 'q' conforme mapeamento de variáveis do template
    r = a  # Resto inicializado com o dividendo original

    # # 2. ASSERCAO DE INICIALIZACAO (CASO BASE)
    # Valida se o invariante é válido imediatamente antes de entrar no laço
    assert a_orig == y * b + r and r >= 0, "Erro: Invariante falhou na inicializacao!"

    # Laço principal (enquanto a guarda do loop for verdadeira)
    while r >= b:

        # # Mapeamento da Função Variante (Antes das modificações)
        velha_variante = r

        # Valida se a variante respeita o limite inferior
        assert velha_variante >= 0, "Erro: Variante violou o limite inferior!"

        # # Corpo do algoritmo com as modificações de estado
        r = r - b
        y = y + 1

        # # 3. ASSERCAO DE MANUTENCAO (PASSO INDUTIVO)
        # Comprova que a propriedade se preserva da iteração k para k + 1
        assert a_orig == y * b + r and r >= 0, "Erro: Invariante violado no corpo do loop!"

        # # 4. ASSERCAO DE DECREMENTO (PROGRESSO DA TERMINACAO)
        # Garante o progresso estrito em direção à parada do laço
        assert r < velha_variante, "Erro: Loop em execucao infinita (sem progresso)!"

    # # 5. ASSERCAO DE POS-CONDICAO (DEDUCAO FINAL)
    # Verificação se o resultado deduzido após o término bate com o esperado
    assert y == a_orig // b and r == a_orig % b, "Erro: A pos-condicao falhou na terminacao!"

    return y, r

